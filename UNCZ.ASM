;	UNCZ.ASM 摘除外部词组
;	1995.9.29

CSEG	SEGMENT
	ASSUME	CS:CSEG, DS:CSEG

	ORG	100H
START:
	MOV	AX,2004H		;取外部词组地址
	INT	16H
	OR	BP,BP
	JNZ	L_1
	MOV	DX,OFFSET NOCZ
	JMP	SHORT L_2
L_1:
	MOV	ES,BP
	MOV	AH,49H			;清联想库
	INT	21H
	MOV	ES,ES:[2CH]
	MOV	AH,49H			;清环境块
	INT	21H
	XOR	BP,BP
	MOV	AX,2000H		;清键盘模块中联想库段
	INT	16H
	MOV	DX,OFFSET OK
L_2:
	MOV	AH,9
	INT	21H
	MOV	AX,4C00H
	INT	21H

OK	DB	'外部词组已经清除!',7,13,10,'$'
NOCZ	DB	'没有安装外部词组!',7,13,10,'$'

CSEG	ENDS
	END	START
