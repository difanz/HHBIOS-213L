;	UNLX.ASM 摘除联想库
;	1995.9.29

CSEG	SEGMENT
	ASSUME	CS:CSEG, DS:CSEG

	ORG	100H
START:
	MOV	AX,2005H		;取联想库地址
	INT	16H
	OR	BP,BP
	JNZ	L_1
	MOV	DX,OFFSET NOLX
	JMP	SHORT L_2
L_1:
	MOV	ES,BP
	MOV	AH,49H			;清联想库
	INT	21H
	MOV	ES,ES:[2CH]
	MOV	AH,49H			;清环境块
	INT	21H
	XOR	BP,BP
	MOV	AX,2001H		;清键盘模块中联想库段
	INT	16H
	MOV	DX,OFFSET OK
L_2:
	MOV	AH,9
	INT	21H
	MOV	AX,4C00H
	INT	21H

OK	DB	'联想库已经清除!',7,13,10,'$'
NOLX	DB	'没有安装联想库!',7,13,10,'$'

CSEG	ENDS
	END	START
